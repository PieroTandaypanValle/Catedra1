    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/surveys", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from medicals where ID_M = ?");
            pst.setString(1, txt_buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                
                txt_id.setText(rs.getString("ID_M"));
                txt_run.setText(rs.getString("RUN_M"));
                txt_dv.setText(rs.getString("DV_M"));
                txt_names.setText(rs.getString("NAMES_M"));
                txt_father.setText(rs.getString("FATHER_NAME_M"));
                txt_mother.setText(rs.getString("MOTHER_NAME_M"));
                txt_gender.setText(rs.getString("GENDER_M"));
                txt_deleted.setText(rs.getString("DELETE_ALL_M"));
                
                
            }else{
                JOptionPane.showMessageDialog(null, "Nombre no registrado.");
            }
        }catch (Exception e){
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

