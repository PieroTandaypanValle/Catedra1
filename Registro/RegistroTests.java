    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/surveys", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into tests values(?,?,?,?,?,?,?)");

            pst.setString(1, txt_id6.getText().trim());
            pst.setString(2, txt_idpacient.getText().trim());
            pst.setString(3, txt_idmedical.getText().trim());
            pst.setString(4, txt_nametest.getText().trim());
            pst.setString(5, txt_ptsmin.getText().trim());
            pst.setString(6, txt_ptsmax.getText().trim());
            pst.setString(7, txt_deleted6.getText().trim());

            pst.executeUpdate();

            txt_id6.setText("");
            txt_idpacient.setText("");
            txt_idmedical.setText("");
            txt_nametest.setText("");
            txt_ptsmin.setText("");
            txt_ptsmax.setText("");
            txt_deleted6.setText("");

            label_status6.setText("Registro exitoso");

        }catch(Exception e){

        }

    }//GEN-LAST:event_jButton1ActionPerformed

