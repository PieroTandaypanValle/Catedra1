    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/surveys", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from tests where ID_TEST= ?");
            pst.setString(1, txt_buscar6.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){

                txt_id6.setText(rs.getString("ID_TEST"));
                txt_idpacient.setText(rs.getString("ID_PAC"));
                txt_idmedical.setText(rs.getString("ID_M"));
                txt_nametest.setText(rs.getString("NAME_TEST"));
                txt_ptsmin.setText(rs.getString("PTS_Min_TEST"));
                txt_ptsmax.setText(rs.getString("PTS_MAX_TEST"));
                txt_deleted6.setText(rs.getString("DELETE_ALL_TEST"));

            }else{
                JOptionPane.showMessageDialog(null, "Nombre no registrado.");
            }
        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton4ActionPerformed

